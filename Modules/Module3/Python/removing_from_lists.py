"""
Demonstrates deleting elements from lists.
"""

#A list named numbers containing the values 8, 5, 7, 3 and 1
numbers = [8, 10, 5, 7, 3, 1, 10]

#Deletes the third element from the numbers list
del numbers[2]

#Prints the numbers list
print(numbers)

#Deletes the third element from the numbers list again
del numbers[2]

#Prints the numbers list
print(numbers)

